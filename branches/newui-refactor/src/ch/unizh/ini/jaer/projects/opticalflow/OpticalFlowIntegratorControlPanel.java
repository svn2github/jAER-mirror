    protected void updateGui() {
        tracesCB.setSelected( integrator.isShowTraces());
        medianCB.setSelected( integrator.isUsingFilter());
        optionCB.setSelected( integrator.isDebugOption());
        param1.setText( integrator.getDebugParam1() );
        param2.setText( integrator.getDebugParam2() );
    }
    
    protected void updateIntegrator() {
        integrator.doShowTraces( tracesCB.isSelected() );
        integrator.setUseFilter( medianCB.isSelected() );
        integrator.setDebugOption( optionCB.isSelected() );
        integrator.setDebugParam1( param1.getText() );
        integrator.setDebugParam2( param2.getText() );
        log.info("updated GUI->OFI");
    }

